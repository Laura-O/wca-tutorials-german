<p>
WCA Competitions werden (in der Regel) ehrenamtlich von Freiwilligen
organisiert. Wenn du also selbst schon oft auf einer Competition warst,
überleg dir, ob es nicht vielleicht fair der Community gegenüber
wäre sich zu revangieren indem du auch so etwas für andere
veranstaltest. Die Leute werden es dir danken und du wirst
viele wertvolle Erfahrungen sammeln!
</p>

<p>
Eine Competition zu organisieren ist keine leichte Aufgabe
(vorallem wenn man finanziell sehr eingeschränkt ist),
aber auch nicht so schwer, wie man vielleicht denkt. Auf dieser Seite
sollen ein paar Tipps gegeben werden und eine Liste von Dingen, die man
beim organisieren einer Meisterschaft beachten sollte.
</p>

<h2>Loslegen</h2>

<ul>
 <li>
  <b>Besuche andere Competitions</b><br/>
  Eine Meisterschaft kann man nur organisieren, wenn man weiss was das ist.
  Bevor du also selbst etwas planst, solltest du mindestens einmal,
  besser aber noch öfter, eine Competition besuchen und dir alles sehr
  genau anschauen. Achte vorallem darauf, welche Aufgaben das
  Organisationsteam übernimmt. Sprich mit den Organisatoren, frag sie,
  welche Probleme sie bei der Organisation hatten, lass dir Tipps geben.
  Ein Textdokument kann nie eigene Erfahrung ersetzen.
 </li>
 <li>
  <b>Stell sicher, dass genug Leute Interesse haben</b><br/>
  Eine gute Competition sollte mindestens 20 Teilnehmer haben
  (und muss mindestens 12 Teilnehmer haben um von der WCA anerkannt zu
  werden). Erkundige dich vorher, z.B. in Internetforen und deinem
  Bekanntenkreis, ob überhaupt Interesse an einer Meisterschaft
  in deinem Ort besteht.
 </li>
 <li>
  <b>Wähle ein Wochenende, an dem möglichst viele Personen können</b><br/>
  Erkundige dich, welche anderen Competitions noch stattfinden. Es ist
  wenig sinnvoll zwei deutsche Competitions an ein und dem selben
  Wochenende zu veranstalten. Frage in der Community, z.B. in Internetforen,
  welche Wochenenden bevorzugt werden.
 </li>
 <li>
  <b>Suche einen guten Raum</b><br/>
  Ein guter Raum ist groß genug für die Anzahl der Personen, die du
  annehmen möchtest. Es sollte leicht möglich sein den Bereich
  in einen Zuschauerbereich, einen Wartebereich und einen
  Wettbewerbsbereich einzuteilen. Der Raum sollte sehr leise sein,
  sehr viel Tageslicht haben, beheizt sein und es sollte nicht
  verboten sein zu essen oder zu trinken.
 </li>
 <li>
  <b>Suche einen WCA Delegate</b><br/>
  Jede offizielle WCA Competition benötigt mindestens einen offiziellen
  Vertreter der WCA, einen sogenannten "Delegate". In Deutschland ist
  das momentan Sébastien Auroux
  (<a href="mailto:sebastien.auroux@gmx.net">sebastien.auroux@gmx.net</a>)
  aus Aachen. Schreibe ihn an und frage ob er an deinem Wunschtermin
  Zeit hat. Falls er keine Zeit hat, frage einen anderen Delegate,
  z.B. Ron van Bruchem
  (<a href="mailto:ron@speedcubing.com">ron@speedcubing.com</a>) aus
  den Niederlanden. Eine Liste aller WCA Delegierten gibt es auf
  der Webseite der WCA: <a href="http://www.worldcubeassociation.org/node/316">http://www.worldcubeassociation.org/node/316</a>.
 </li>
 <li>
  <b>Benachrichtige das WCA Board</b><br/>
  Du solltest nun die WCA benachrichtigen. Teile ihnen mit wann und wo
  deine Competition stattfinden soll, wer der Delegate sein soll,
  wieviele Personen du erwartest und wieviel Eintritt du verlangen
  möchtest.
 </li>
 <li>
  <b>Suche dir genug Hilfe</b><br/>
  Suche dir genug freiwillige Helfer, die auf jeden Fall kommen und dir
  helfen können. Eine Competition zu organisieren ist immer Teamarbeit!
  Alleine wird es schwer bis unmöglich.
 </li>
</ul>

<h2>Sobald du die Erlaubnis der WCA hast ...</h2>

<ul>
 <li>
  <b>Spreche erneut mit dem Vermieter</b><br/>
  Hole dir die Bestätigung vom Vermieter, dass du den Raum definitiv zu
  dem abgesprochenen Termin haben kannst. Erzähle ihm genau was du vor hast
  und wieviele Personen kommen werden. Kläre rechtzeitig ob genug Tische
  und Stühle vorhanden sind, dass Toiletten existieren und dass
  gegessen und getrunken werden darf.
 </li>
 <li>
  <b>Erstelle einen Zeitplan</b><br/>
  Überlege dir gut, wann welches Event stattfinden soll, wie lange
  es ungefähr dauern wird, wieviele Leute in die nächste Runde kommen
  werden, etc. Spreche diesen Zeitplan mit dem WCA Delegate ab und frag
  ihn ob er deinen Zeitplan für realistisch hält.
 </li>
 <li>
  <b>Erstelle eine Webseite</b><br/>
  Jede Meisterschaft sollte eine Webseite haben auf der sich Teilnehmer
  im vorraus anmelden können (damit du weisst, wieviele kommen) und auf
  der alle nötigen Informationen (Termin, Ort, Zeitplan, Eintrittspreis)
  angekündigt werden. Du solltest auch eine Liste von Hotels in der Nähe
  angeben. Falls du nicht weisst, wie man eine Webseite erstellt,
  melde dich per E-Mail bei Florian Weingarten
  (<a href="mailto:flo@hackvalue.de">flo@hackvalue.de</a>).
 </li>
 <li>
  <b>Kontaktiere das WCA Board erneut</b><br/>
  Teile dem WCA Board bzw. dem Delegate die Adresse der Webseite mit
  und bestätige ihm, dass die Meisterschaft wirklich stattfinden soll.
 </li>
</ul>

<h2>Vor der Competition</h2>

<ul>
 <li>
  <b>Kündige die Competition in der Community an</b><br/>
  Jetzt ist es an der Zeit, kräftig Werbung zu machen. Nicht jeder
  Speedcuber liest regelmäßig die WCA Webseite, daher solltest du in
  möglichst vielen Internetforen deine Meisterschaft ankündigen
  (z.B. speedcube.de, speedcubers.de und speedsolving.com). Mache
  an so vielen Stellen wie möglich Werbung um möglichst viele
  Teilnehmer und Zuschauer anzulocken.
 </li>
 <li>
  <b>Kontaktiere die Medien</b><br/>
  Rede mit der Presse. Schreibe verschiedene Fernsehsender, Radiosender,
  Zeitungen, Magazine, Schülerzeitungen, etc. an und lade sie ein
  deine Meisterschaft zu besuchen und darüber zu berichten.
 </li>
 <li>
  <b>Besorge Verpflegung</b><br/>
  Falls es finanziell machbar ist, solltest du kostenlose oder zumindest
  preisgünstige Getränke und Snacks oder sogar richtiges Mitagessen
  anbieten. Falls dies nicht möglich ist, solltest du das rechtzeitig
  ankündigen, damit die Besucher ihr eigenes Essen mitbringen. Aufjedenfall
  sollte eine Mittagspause im Zeitplan vorgesehen sein!
 </li>
 <li>
  <b>Bereite das Excel Worksheet vor</b><br/>
  Mache dich mit Microsoft Excel (oder OpenOffice Spreadsheet) vertraut.
  Die Ergebnisse der Meisterschaft werden in einer Excel Tabelle notiert
  und deren Benutzung ist teilweise nicht ganz leicht. Bitte deinen
  Delegate dir eine Kopie der Datei zuzusenden.
 </li>
 <li>
  <b>Installier die Scramble Programme</b><br/>
  Es kann passieren, dass du während der Meisterschaft zusätzliche Scrambles
  benötigst. Installiere dir daher die Scramble Programme (siehe WCA Regeln)
  auf deinem Computer.
 </li>
 <li>
  <b>Drucke die Scoresheets und die Teilnehmerliste</b><br/>
  Du solltest dich rechtzeitig darum kümmern alle Scoresheets
  (Zettel, auf denen die Schiedsrichter die Ergebnisse notieren) zu drucken.
  An der Kasse im Eingangsbereich sollte ausserdem eine Teilnehmerliste
  bereit liegen und eventuell für jeden Teilnehmer ein Namensschild.
 </li>
 <li>
  <b>Besorge Preise</b><br/>
  Zumindest die Gewinner der Hauptkategorien sollten Preise erhalten.
  Eine Urkunde ist ein Muss. Pokale, Medallien oder andere Preise sind
  optional, aber immer eine nette Geste.
 </li>
 <li>
  <b>Besorge die Scrambles</b><br/>
  Kläre rechtzeitig mit dem Delegate ob er die Scrambles mitbringt
  oder du sie selber drucken musst. Falls du sie drucken sollst,
  tue dies rechtzeitig und nicht erst am Abend vorher!
 </li>
 <li>
  <b>Lerne die WCA Regeln</b><br/>
  Der WCA Delegierte kennt alle Regeln perfekt, aber auch der Organisator
  sollte ausreichend vertraut sein und die gesamten Regeln mehrmals
  aufmerksam durchgearbeitet und verstanden haben. Sollten einzelne
  Teile unklar sein, frage den WCA Delegate ob er sie genauer erklären
  kann.
 </li>
</ul>


<h2>Während der Competition</h2>

<ul>
 <li>
  <b>Rechtzeitig da sein</b><br/>
  Als Veranstalter solltest du am besten am Abend vorher oder zur Not
  morgens vor der Competition den Raum herrichten. Stelle viele Tische
  und Stühle auf, bereite den Wartebereich vor, überlege dir, wo du die
  Timer und Displays aufbauen möchtest, usw. Sei rechtzeitig da, damit
  alles nach Zeitplan verlaufen kann. Sobald du einmal im Zeitplan
  hinterher bist, wird es sehr schwer werden die Zeit aufzuholen und du
  wirst eventuell einzelne Events streichen müssen!
 </li>
 <li>
  <b>Scrambler und Judges</b><br/>
  Zuwenig Scrambler und zuwenig Judges sind der Hauptgrund, warum
  Competitions ihren Zeitplan nicht einhalten können. Sorge dafür,
  dass genug Helfer für jedes Event vorhanden sind!
 </li>
 <li>
  <b>Ergebnisse eintragen</b><br/>
  Suche dir einen Helfer, der sich mit Microsoft Excel auskennt und
  der die Ergebnisse eintragen kann. Das ist ein Fulltime Job! Es wird
  rund um die Uhr jemand am PC sitzen müssen und Ergebnisse eintragen,
  damit rechtzeitig die Ranglisten einer Runde fertig sind (um zu ermitteln,
  wer in die nächste Runde kommt).
 </li>
 <li>
  <b>Achte auf die Regeln</b><br/>
  Werden Regeln verletzt, kann der WCA Delegate die Competition für
  ungültig erklären und das wird alle Teilnehmer sehr verärgern. Sorge
  also stets dafür, dass alle Regeln genau eingehalten werden.
 </li>
 <li>
  <b>Kopie der Regeln</b><br/>
  Da die wenigsten Leute alle Regeln auswendig kennen, solltest du mehrere
  Kopien der Regeln (am besten in der englischen Originalversion und auch
  als deutsche Übersetzung) vor Ort haben um im Zweifelsfall nachlesen
  zu können.
 </li>
 <li>
  <b>Mache die Meisterschaft einzigartig</b><br/>
  Überleg dir, wie du deine Competition von anderen Competitions
  unterscheiden kannst und wie du dich hervorheben kannst. Z.B. durch
  besondere und seltene Events, durch besonders gutes Essen, besondere
  Preisregelungen (z.B. "alle Newcomer dürfen umsonst rein") oder andere
  Dinge.
 </li>
</ul>


<h2>Was du brauchen wirst</h2>

<ul>
 <li>
  <b>Computer und Drucker</b><br/>
  Einen Computer, am besten Laptop, sowie einen Drucker sind unbedingt
  nötig für eine Competition. Am PC wirst du die Ergebnisse in das
  Excel Sheet eintragen müssen. Mit dem Drucker druckst du Ergebnisse,
  Urkunden und andere Dinge. Die Ergebnisse solltest du in mehrfacher
  Ausführung an verschiedenen Stellen im Raum aushängen, damit die Leute
  nicht ständig am PC nach ihrer Platzierung fragen.
 </li>
 <li>
  <b>Stackmat Timer und Displays</b><br/>
  Du wirst mehrere Timer und am besten auch Displays, damit die Zuschauer
  die Zeiten sehen können, benötigen. Diese sind sehr teuer, daher solltest
  du beim Delegate anfragen, ob es möglich ist welche auszuleihen.
 </li>
 <li>
  <b>Batterien</b><br/>
  Die Displays verbrauchen Strom. Sorge dafür, dass ausreichend
  Ersatzbatterien vorhanden sind.
 </li>
 <li>
  <b>Stoppuhren</b><br/>
  Um die Inspection Zeit zu stoppen, werden Stoppuhren benötigt.
  Pro Timer sollte eine Stoppuhr zur Verfügung stehen.
 </li>
 <li>
  <b>Abdeckungen</b><br/>
  Um die gemischten Puzzle zu verdecken, sollte irgendeine Art von
  Abdeckung vorhanden sein, z.B. Plastikdeckel oder Papierblätter.
 </li>
 <li>
  <b>Scrambles</b><br/>
  Auf der WCA Webseite findest du die Programme zum Generieren der
  Scrambles. Spreche rechtzeitig mit dem Delegate ab, ob du diese
  Scrambles selbst generieren und drucken musst oder ob er sie bereit
  stellt. Die Scrambles müssen unbedingt streng geheim gehalten werden
  und sollten in einem verschlossenen Umschlag aufbewahrt werden.
 </li>
 <li>
  <b>Scoresheets</b><br/>
  Dies sind Zettel, auf denen die Ergebnisse eines Teilnehmers in einem
  Event eingetragen werden und können aus der Excel Datei generiert werden.
 </li>
 <li>
  <b>Stifte</b><br/>
  Um die Ergebnisse zu notieren sollte bei jedem Timer mindesten ein
  Kugelschreiber bereit liegen. Halte genug Ersatzkulis bereit.
  (Ein nettes Extra sind Kugelschreiber mit eigenem Logo o.ä.)
 </li>
</ul>

<h2>Häufig gestellte Fragen</h2>

<p>
<b>F:</b> Wie kann man abschätzen, was die Veranstaltung kosten wird? <br/>
<b>A:</b> Das teuerste wird vermutlich der Raum. Wenn du den Raum irgendwie
umsonst bekommen kannst (Gemeindezentrm der Kirche? Jugendzentrum?
Universität? Schule?) dann wird es nicht so teuer. Vergiss aber nicht
an andere variable Kosten wie z.B. das Essen und die Getränke zu denken.
</p>

<p>
<b>F:</b> Wie bezahlt man das alles? <br/>
<b>A:</b> Normalerweise kann man die Competition gut so planen, dass
man bei Null rauskommt. Falls nicht, solltest du dich um sponsoren
bemühen (z.B. Jumbo Spiele oder Cubikon). Es besteht aber natürlich
ein Restrisiko, dass du auf Kosten sitzen bleibst (z.B. weil
weniger Personen erscheinen als erwartet).
</p>

<p>
<b>F:</b> Woher weiss man wieviele Leute ungefähr kommen? <br/>
<b>A:</b> Schaue dir an, wie gut Competitions in deiner Nähe normalerweise
besucht sind. Spreche mit der Community. Starte Umfragen in Internet Foren.
Behalte im Hinterkopf, dass so gut wie nie alle Leute erscheinen, die
 angemeldet sind, dafür aber auch oft Zuschauer oder Gäste unangemeldet
aufkreuzen. <br/>
</p>

<p>
<b>F:</b> Woher weiss ich, welcher Termin am besten ist? <br/>
<b>A:</b> An deinem Termin sollten keine anderen Competitions in der Nähe
stattfinden. Am
besten auch an dem Wochenende davor und danach nicht. Sprich mit der
Community und starte eventuell Umfragen, welche Termine am beliebtesten sind.
</p>

<p>
<b>F:</b> Wo bekomme ich das ganze Equipment? <br/>
<b>A:</b> Du könntest es kaufen, was sich besonders dann lohnt, wenn du vor
hast noch öfter eine Competition zu organiseren. Meistens ist das aber
zu teuer. Sprich mit dem Delegate oder anderen Helfern, ob sie dir
aushelfen können. Der Delegate kann in der Regel die Timer und Displays
besorgen.
</p>

<p>
<b>F:</b> Sollte ich den WCA Delegate bezahlen? <br/>
<b>A:</b> WCA Delegate ist ein Ehrenamt. Delegates werden nicht bezahlt,
aber die Anreisekosten zu übernehmen oder eine kostenlose
Übernachtungsmöglichkeit sowie ein kleines Dankeschöngeschenk sind eine
nette Geste.
</p>

